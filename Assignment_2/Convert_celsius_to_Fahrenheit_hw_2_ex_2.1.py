#Home Work_2_Ex_2.1 (convert Celsius to Fahrenheit)

#First prompt the user for celsius value

celsius=eval(input("Enter a degree in Celsius:",))

#Then convert Celsius to Fahrenheit using formulae  

fahrenheit = (9 / 5) * celsius + 32

#Lastly print the two values

print(celsius, " celsius is ",fahrenheit, " fahrenheit.")

